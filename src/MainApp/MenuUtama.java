    // Menampilkan database nasabah dalam bentuk tabel
    public void datatable(){
        
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("No. PK");
        tbl.addColumn("Nama");
        tbl.addColumn("Telepon");
        tbl.addColumn("Alamat");
        tbl.addColumn("No. Polisi");
        tbl.addColumn("Kendaraan");
        tbl.addColumn("Tahun");
        tbl.addColumn("Pinjaman");
        tbl.addColumn("Tenor");
        tbl.addColumn("Jatuh tempo");
        tbl.addColumn("Surveyor");
        jTable1.setModel(tbl);
        try{
            Statement statement = (Statement)konek_nasabah.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM nasabah");
            while (res.next())
            {
                tbl.addRow(new Object[]{
                    res.getString("no_pk"),
                    res.getString("nama"),
                    res.getString("no_telp"),
                    res.getString("alamat"),
                    res.getString("nopol"),
                    res.getString("kendaraan"),
                    res.getString("tahun"),
                    res.getString("jumlah_pinjaman"),
                    res.getString("tenor"),
                    res.getString("jatuh_tempo"),
                    res.getString("surveyor"),
                });
                jTable1.setModel(tbl);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane,e.getMessage());
        }
    }

