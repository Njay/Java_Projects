    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // menghapus data dari database nasabah
        String no_pk = jPk.getText();
        try{
            Statement statement = (Statement) konek_nasabah.GetConnection().createStatement();
            statement.executeUpdate("DELETE FROM nasabah where no_pk=('"+no_pk+"');");
        JOptionPane.showMessageDialog(null, "Data berhasil di Hapus!");
        jPk.setText("");
        jPk.requestFocus();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        datatable();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Mencari data nasabah berdasarkan nomor pk
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("No. PK");
        tbl.addColumn("Nama");
        tbl.addColumn("Telepon");
        tbl.addColumn("Alamat");
        tbl.addColumn("No. Polisi");
        tbl.addColumn("Kendaraan");
        tbl.addColumn("Tahun");
        tbl.addColumn("Pinjaman");
        tbl.addColumn("Tenor");
        tbl.addColumn("Jatuh tempo");
        tbl.addColumn("Surveyor");
        jTable1.setModel(tbl);          
            try {                
                String sql = " SELECT * FROM nasabah WHERE no_pk like '%"+jPk.getText()+"%'";
                Connection con = (Connection) konek_nasabah.GetConnection();
                java.sql.Statement st = con.createStatement();
                ResultSet res = st.executeQuery(sql);  
            while (res.next()){
                tbl.addRow(new Object[]{
                    res.getString("no_pk"),
                    res.getString("nama"),
                    res.getString("no_telp"),
                    res.getString("alamat"),
                    res.getString("nopol"),
                    res.getString("kendaraan"),
                    res.getString("tahun"),
                    res.getString("jumlah_pinjaman"),
                    res.getString("tenor"),
                    res.getString("jatuh_tempo"),
                    res.getString("surveyor"),                   
                });
                jTable1.setModel(tbl);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

